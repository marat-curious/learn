        The most common way to create range is `..`

        val r1 = 1..4 // 1,2,3,4

        To declare range that doesn't include end value, use the `..<` operator

        val r2 = 1..<4 // 1,2,3

        To declare a range in reverse order, use `downTo`

        val r3 = 4 downTo 1 // 4,3,2,1

        To declare a range that increments in a step that isn't 1, use `step`

        val r4 = 1..5 step 2 // 1,3,5

        Same with `Char` ranges

        val c1 = 'a'..'d' // 'a','b','c','d'

        val c2 = 'z' downTo 's' step 2 // 'z','x','v','t'
