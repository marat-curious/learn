#include <iostream>

int main() {
    std::cout << "int8_t / uint8_t: " << sizeof(int8_t) << " / " << sizeof(uint8_t) << "\n";
    std::cout << "int16_t / uint16_t: " << sizeof(int16_t) << " / " << sizeof(uint16_t) << "\n";
    std::cout << "int32_t / uint32_t: " << sizeof(int32_t) << " / " << sizeof(uint32_t) << "\n";
    std::cout << "int64_t / uint64_t: " << sizeof(int64_t) << " / " << sizeof(uint64_t) << "\n";
    return 0;
}
